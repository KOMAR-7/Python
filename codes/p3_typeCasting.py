# Integers
a = int(1)   # x will be 1
b = int(2.8) # y will be 2
c = int("3") # z will be 3

# Float
d = float(1)     # x will be 1.0
e = float(2.8)   # y will be 2.8
f = float("3")   # z will be 3.0
g = float("4.2") # w will be 4.2

# String
h = str("s1") # x will be 's1'
i = str(2)    # y will be '2'
j = str(3.0)  # z will be '3.0'

print("type of a: ", type(a))
print("type of b: ", type(b))
print("type of c: ", type(c))
print("type of d: ", type(d))
print("type of e: ", type(e))
print("type of f: ", type(f))
print("type of g: ", type(g))
print("type of h: ", type(h))
print("type of i: ", type(i))
print("type of j: ", type(j))

# type of a:  <class 'int'>
# type of b:  <class 'int'>  
# type of d:  <class 'float'>
# type of e:  <class 'float'>
# type of f:  <class 'float'>
# type of g:  <class 'float'>
# type of h:  <class 'str'>
# type of i:  <class 'str'>
# type of j:  <class 'str'>