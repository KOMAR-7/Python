# you can use slicing technique here also
fruit = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(fruit[2:5]) # ['cherry', 'orange', 'kiwi']
print(fruit[:4]) # ['apple', 'banana', 'cherry', 'orange']
print(fruit[2:]) # ['cherry', 'orange', 'kiwi', 'melon', 'mango']
print(fruit[0::2]) # ['apple','cherry','kiwi','mango']
print(fruit[-4:-1]) # ['orange', 'kiwi', 'melon']